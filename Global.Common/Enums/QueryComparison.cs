
namespace Global.Common.Enums
{
    /// <summary>
    /// Specifies the types of query comparison operations that can be used in a query.
    /// </summary>
    public enum QueryComparison
    {
        /// <summary>
        /// Equal to (Eq)
        /// </summary>
        eq,

        /// <summary>
        /// Not equal to (Ne)
        /// </summary>
        ne,

        /// <summary>
        /// Greater than (Gt)
        /// </summary>
        gt,

        /// <summary>
        /// Greater than or equal to (Ge)
        /// </summary>
        ge,

        /// <summary>
        /// Less than (Lt)
        /// </summary>
        lt,

        /// <summary>
        /// Less than or equal to (Le)
        /// </summary>
        le,
    }
}
